/* Métodos HTTP:
 - GET:    buscar (pegar) uma informação do back-end
 - POST:   criar uma informação no back-end
 - PUT:    alterar uma informação no back-end
 - DELETE: deleta uma informação no back-end
 */

/* Tipos de parâmetros:
- Query:        parâmetros nomeados na rota após o ? (Filtros, paginação)
- Route Params: parâmetros utilizados para identificar recursos
- Request Body: corpo da requisição utilizado para criar ou alterar recursos
*/

/* Tipos de Bancos de Dados
- SQL:   MySQL, PostgreSQL, ->SQLite, Oracle, Microsoft SQL Server
- NoSQL: MongoDB, CouchDB...
*/

/*Tipos de instalação do banco de dados
- Driver:        SELECT * FROM users.
- Query Builder: utilizamos sintaxe do JS: 
                 tabele('users').select(*).where(...)
*/